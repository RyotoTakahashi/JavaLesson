package javalesson01;

public class JavaLesson1_2_Others {

	public static void main(String[] args) {
		int num1 = 8;
		int num2 = 3;
		System.out.println(num1 + "+" + num2 + "=" + (num1 + num2) + "です");
		num1 = 10;
		num2 = 4;
		System.out.println(num1 + "-" + num2 + "=" + (num1 - num2) + "です");
		num1 = 6;
		num2 = 7;
		System.out.println(num1 + "×" + num2 + "=" + (num1 * num2) + "です");
		String name = "髙橋嶺斗";
		int age = 22;
		age += 1;
		System.out.println(name + "は次の誕生日で" + age + "歳です。");
		num1 = 8;
		num2 = 3;
		System.out.println(num1 + "+" + num2 + "=" + (num1 + num2) + "です");
		System.out.println(num1 + "-" + num2 + "=" + (num1 - num2) + "です");
		System.out.println(num1 + "×" + num2 + "=" + (num1 * num2) + "です");
		System.out.println(num1 + "÷" + num2 + "=" + (num1 / num2) + "、余りは" + (num1 % num2) + "です");
	}

}
